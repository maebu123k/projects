
public class CubNomination {
	//VARIABLES
	String name, tribeName;
	
	//CONSTRUCTOR
	CubNomination(String name, String tribeName){
		this.name = name;
		this.tribeName = tribeName;
	}
	
	//NAME ACCESSOR METHOD
	public String getName() {
		return this.name;
	}
	
	//TRIBE NAME ACCESSOR METHOD
	public String getTribeName() {
		return this.tribeName;
	}
}
